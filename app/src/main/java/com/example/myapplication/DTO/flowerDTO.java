package com.example.myapplication.DTO;
public class flowerDTO {
    private String f_nick; //식물 닉네임
    private String f_date; //식물 심은 날짜
    private String f_shape; //식물 이미지
    private String f_name; //식물 찐이름

    public String getF_nick() {
        return f_nick;
    }
    public void setF_nick(String f_nick) {
        this.f_nick = f_nick;
    }
    public String getF_date() {
        return f_date;
    }
    public void setF_date(String f_date) {
        this.f_date = f_date;
    }
    public String getF_shape() {
        return f_shape;
    }
    public void setF_shape(String f_shape) {
        this.f_shape = f_shape;
    }
    public String getF_name() {
        return f_name;
    }
    public void setF_name(String f_name) {
        this.f_name = f_name;
    }
}
